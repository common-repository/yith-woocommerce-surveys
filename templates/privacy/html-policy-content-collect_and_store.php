<p>The plugin allows displaying short surveys on the website pages.
    The plugin stores the answers given by the users to prevent showing the same survey again.
</p>
<p>
    If the user has registered to the store, the details of surveys submitted will be stored in a user meta, while if the user is visiting the site with a guest account, these data (survey ID, question and answer) will be stored in a cookie.
</p>
<p>
    The plugin allows showing surveys on the checkout page. In such a case, the answers will be stored also in the order details and therefore are subject to WooCommerce Privacy Policy.
</p>